# While loops 
# ask a question again and again 
# usually a boolean value 

# declare i as variable
# start while loop
# while i does not equal 0 then print meow
# when i equals 0 then stop the loop ---- this will stop your computer from breaking from the infinite loop

i = 3 
while i != 0:
    print("meow")
    i = i - 1




